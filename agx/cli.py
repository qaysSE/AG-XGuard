@main.command("list-vaccines")
def list_vaccines() -> None:
    """Show all active vaccines from ~/.agx/vaccines/."""
    from agx.store import get_store

    store = get_store()
    manifests = store.list_all_vaccines()

    if not manifests:
        console.print("[dim]No vaccine files found in ~/.agx/vaccines/[/dim]")
        console.print("Run [cyan]agx scan[/cyan] to generate vaccines.")
        return

    for manifest in manifests:
        console.print(
            f"\n[bold cyan]{manifest.agent_name}[/bold cyan] "
            f"[dim](v{manifest.version}, {len(manifest.vaccines)} vaccine(s))[/dim]"
        )
        for vaccine in manifest.vaccines:
            console.print(f"  [dim]{vaccine.id}[/dim]  {vaccine.failure_category.value}  "
                          f"confidence={int(vaccine.confidence * 100)}%")
            if vaccine.executable_assertions:
                for a in vaccine.executable_assertions:
                    sev_style = "red" if a.severity.value == "BLOCK" else "yellow"
                    console.print(
                        f"    [{sev_style}]{a.severity.value}[/{sev_style}] "
                        f"{a.engine.value} → {str(a.pattern)[:60]}"
                    )


