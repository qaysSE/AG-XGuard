@main.command()
@click.option("--input", "-i", "input_file", required=True, help="JSONL or plain-text log file")
@click.option("--agent", "-a", "agent_name", default=None, help="Filter to one agent name")
@click.option("--output", "-o", "output_file", default=None, help="Write vaccines to YAML file")
@click.option("--dry-run", is_flag=True, default=False, help="Print report without writing files")
@click.option(
    "--exit-code",
    is_flag=True,
    default=False,
    help="Exit non-zero if BLOCK-level violations detected (CI/CD mode)",
)
def scan(
    input_file: str,
    agent_name: Optional[str],
    output_file: Optional[str],
    dry_run: bool,
    exit_code: bool,
) -> None:
    """Analyze a log file for agent failure patterns and suggest vaccines."""
    from agx.scanner.analyzer import analyze
    from agx.scanner.yaml_exporter import export_yaml
    from agx._models import VaccineManifest, AssertionSeverity

    try:
        report = analyze(input_file, agent_name=agent_name)
    except FileNotFoundError as exc:
        console.print(f"[red]Error:[/red] {exc}")
        sys.exit(1)
    except Exception as exc:
        console.print(f"[red]Scan failed:[/red] {exc}")
        sys.exit(1)

    # --- Build rich table ---
    title = (
        f"AGX Scan Report — [bold]{report.agent_name}[/bold] — "
        f"{report.total_runs} runs ({report.failure_count} failures)"
    )

    table = Table(
        title=title,
        box=box.ROUNDED,
        title_style="bold cyan",
        header_style="bold white on dark_blue",
        show_lines=True,
    )
    table.add_column("Pattern", style="cyan", min_width=20)
    table.add_column("Count", justify="right", min_width=10)
    table.add_column("Suggested Action", min_width=25)

    for pattern in report.patterns:
        count_str = f"{pattern.count} ({pattern.percentage}%)"
        if pattern.suggested_assertion:
            sev = pattern.suggested_assertion.severity.value
            eng = pattern.suggested_assertion.engine.value
            action_style = "red bold" if sev == "BLOCK" else "yellow"
            action = Text(f"{eng} {sev}", style=action_style)
        else:
            action = Text("—", style="dim")
        table.add_row(pattern.category.value, count_str, action)

    console.print()
    console.print(table)

    # --- Heuristic confidence disclaimer (refinement #2) ---
    console.print(
        Panel(
            "[yellow]⚠️  Heuristic-based suggestions.[/yellow] "
            "Run [cyan]agx validate[/cyan] against historical outputs\n"
            "to measure false positive rate before committing to production.\n"
            "For ML-powered vaccine generation with backtesting, upgrade to [bold]AG-X Cloud[/bold].",
            border_style="yellow",
            padding=(0, 2),
        )
    )

    # --- Write output ---
    if report.suggested_vaccines and not dry_run:
        dest = output_file
        if dest is None:
            from agx._config import settings
            vd = settings.vaccines_dir
            if vd:
                dest = str(vd / f"{report.agent_name}.yaml")

        if dest:
            manifest = VaccineManifest(
                agent_name=report.agent_name,
                vaccines=report.suggested_vaccines,
            )
            try:
                export_yaml(manifest, dest)
                console.print(f"\n[green]Vaccines written to:[/green] {dest}")
                console.print(f"[dim]Run: agx validate --vaccine {dest}[/dim]")
            except Exception as exc:
                console.print(f"[red]Failed to write vaccines:[/red] {exc}")
    elif dry_run:
        console.print("\n[dim](dry-run mode — vaccines not written)[/dim]")

    # --- Optional LLM enhancement ---
    import os
    if os.environ.get("OPENAI_API_KEY") or os.environ.get("GROQ_API_KEY"):
        try:
            from agx.scanner.llm_doctor import enhance_with_llm
            extra = enhance_with_llm(report, report.agent_name)
            if extra:
                console.print(f"\n[cyan]LLM doctor added {len(extra)} additional vaccine(s).[/cyan]")
        except Exception:
            pass

    # --- CI exit code (refinement #4) ---
    if exit_code and report.has_block_violations:
        console.print(
            "\n[red bold]✗ BLOCK-level violations detected. Exiting with code 1 (CI mode).[/red bold]"
        )
        sys.exit(1)


