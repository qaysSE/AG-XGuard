@main.command()
@click.option("--vaccine", "-v", "vaccine_file", required=True, help="Vaccine YAML file")
@click.option("--test-output", "-t", "test_output", required=True, help="JSON string or plain text to validate")
def validate(vaccine_file: str, test_output: str) -> None:
    """Run cage assertions from a vaccine file against a sample output."""
    from agx.cage import DeterministicCage
    from agx.scanner.yaml_exporter import import_yaml

    try:
        manifest = import_yaml(vaccine_file)
    except FileNotFoundError as exc:
        console.print(f"[red]Error:[/red] {exc}")
        sys.exit(1)
    except Exception as exc:
        console.print(f"[red]Failed to load vaccine:[/red] {exc}")
        sys.exit(1)

    assertions = []
    for vaccine in manifest.vaccines:
        assertions.extend(vaccine.executable_assertions)

    if not assertions:
        console.print("[yellow]No executable assertions found in vaccine file.[/yellow]")
        return

    cage = DeterministicCage(assertions=assertions)
    result = cage.run(test_output)

    table = Table(
        title=f"Cage Validation — {manifest.agent_name}",
        box=box.ROUNDED,
        title_style="bold cyan",
        header_style="bold",
    )
    table.add_column("Engine", style="cyan", min_width=15)
    table.add_column("Target", min_width=14)
    table.add_column("Severity", min_width=10)
    table.add_column("Result", min_width=8)
    table.add_column("Message")

    for verdict in result.verdicts:
        a = verdict.assertion
        passed_icon = "[green]✓ PASS[/green]" if verdict.passed else "[red]✗ FAIL[/red]"
        sev_style = "red" if a.severity.value == "BLOCK" else "yellow"
        table.add_row(
            a.engine.value,
            a.target.value,
            Text(a.severity.value, style=sev_style),
            Text(passed_icon),
            verdict.message,
        )

    console.print()
    console.print(table)

    if result.passed:
        console.print("\n[bold green]✓ All assertions passed.[/bold green]")
    else:
        failed = [v for v in result.verdicts if not v.passed]
        console.print(f"\n[bold red]✗ {len(failed)} assertion(s) failed.[/bold red]")
        if result.blocked:
            console.print("[red]Output would be BLOCKED by the guard.[/red]")
            sys.exit(1)


