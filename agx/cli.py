@main.command()
def init() -> None:
    """Create ~/.agx/ directory structure and a sample vaccines.yaml."""
    from agx._config import settings

    data_dir = settings.resolved_data_dir
    if data_dir is None:
        console.print("[yellow]AGX_DATA_DIR is empty — running in memory mode. Nothing to init.[/yellow]")
        return

    data_dir.mkdir(parents=True, exist_ok=True)
    vaccines_dir = data_dir / "vaccines"
    vaccines_dir.mkdir(exist_ok=True)

    sample_path = vaccines_dir / "sample_agent.yaml"
    if not sample_path.exists():
        sample_path.write_text(
            _SAMPLE_VACCINE_YAML, encoding="utf-8"
        )
        console.print(f"[green]Created sample vaccine:[/green] {sample_path}")
    else:
        console.print(f"[dim]Sample vaccine already exists:[/dim] {sample_path}")

    console.print(f"\n[bold green]✓ AG-X initialized:[/bold green] {data_dir}")
    console.print("\nNext steps:")
    console.print("  1. Decorate your agent:  [cyan]@agx.protect(agent_name='my_agent')[/cyan]")
    console.print("  2. Scan your logs:       [cyan]agx scan --input logs.jsonl[/cyan]")
    console.print("  3. Open the dashboard:   [cyan]agx serve[/cyan]")


