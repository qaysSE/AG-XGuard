@main.command()
@click.option("--limit", "-n", default=50, show_default=True, help="Number of runs to show")
@click.option("--agent", "-a", default=None, help="Filter by agent name")
def runs(limit: int, agent: Optional[str]) -> None:
    """Show recent agent runs from the local SQLite store."""
    from agx.store import get_store

    async def _fetch():
        store = get_store()
        return await store.list_runs(agent_name=agent, limit=limit)

    rows = asyncio.run(_fetch())

    if not rows:
        console.print("[dim]No runs recorded yet.[/dim]")
        console.print("Decorate an agent with [cyan]@agx.protect[/cyan] to start tracing.")
        return

    table = Table(
        title=f"Recent Runs (last {limit})",
        box=box.ROUNDED,
        title_style="bold cyan",
        header_style="bold",
    )
    table.add_column("Agent", style="cyan", min_width=16)
    table.add_column("Outcome", min_width=10)
    table.add_column("Cage", min_width=6, justify="center")
    table.add_column("Duration", justify="right", min_width=10)
    table.add_column("Timestamp", min_width=19)

    for row in rows:
        outcome = row.get("outcome", "—")
        outcome_style = {
            "SUCCESS": "green",
            "FAILURE": "red",
            "BLOCKED": "dark_orange",
            "WARNED": "yellow",
        }.get(outcome, "white")

        cage_passed = row.get("cage_passed")
        cage_icon = "✓" if cage_passed == 1 else ("✗" if cage_passed == 0 else "—")
        cage_style = "green" if cage_passed == 1 else ("red" if cage_passed == 0 else "dim")

        ms = row.get("total_ms")
        ms_str = f"{ms:.0f}ms" if ms is not None else "—"

        ts = (row.get("timestamp") or "")[:19]

        table.add_row(
            row.get("agent_name", "—"),
            Text(outcome, style=outcome_style),
            Text(cage_icon, style=cage_style),
            ms_str,
            ts,
        )

    console.print()
    console.print(table)
