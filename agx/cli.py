@main.command()
@click.option("--port", "-p", default=7000, show_default=True, help="Dashboard port")
@click.option("--host", default="127.0.0.1", show_default=True, help="Bind host")
def serve(port: int, host: str) -> None:
    """Start the local AG-X dashboard."""
    try:
        from agx.dashboard.server import run as run_server
    except ImportError:
        console.print(
            "[red]Dashboard dependencies not installed.[/red]\n"
            "Run: [cyan]pip install agx-community[dashboard][/cyan]"
        )
        sys.exit(1)

    console.print(
        Panel(
            f"[bold green]AG-X Dashboard[/bold green]\n\n"
            f"  [cyan]http://{host}:{port}/[/cyan]         Runs list\n"
            f"  [cyan]http://{host}:{port}/vaccines[/cyan]  Active vaccines\n"
            f"  [cyan]http://{host}:{port}/scanner[/cyan]   Log scanner\n\n"
            f"Press [bold]Ctrl+C[/bold] to stop.",
            border_style="green",
            title="🛡️ AG-X Community",
        )
    )
    run_server(host=host, port=port)


